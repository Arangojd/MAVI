/*
 * mavi-pins.hpp
 * -------------
 *
 * Contains the pin # definitions for MAVI's component connections on the RPi GPIO array.
 * These are Broadcom Rev. 2 pin #'s. See http://wiringpi.com/pins for more details.
 */

#ifndef MAVI_PINS_H
#define MAVI_PINS_H

#define MAVI_PIN_IRS 2
#define MAVI_PIN_IRM 3
#define MAVI_PIN_IRL 4

#define MAVI_PIN_USL 22
#define MAVI_PIN_USR 27

#define MAVI_PIN_VL 14
#define MAVI_PIN_VC 15
#define MAVI_PIN_VR 18

#define MAVI_PIN_PAUSE 23
#define MAVI_PIN_POWER 24

#endif
